		uint64_t* native_call() {
			uint64_t online_natives[] = {
				0xA4A79DD2D9600654, // NETWORK_GET_NUM_CONNECTED_PLAYERS
				0xA6C90FBC38E395EE, // NETWORK_GET_NUM_PARTICIPANTS
				0xCF61D4B4702EE9EB, // NETWORK_GET_TOTAL_NUM_PLAYERS
				0x10FAB35428CCC9D7, // NETWORK_IS_GAME_IN_PROGRESS
				0xCA97246103B63917, // NETWORK_IS_IN_SESSION
				0xB8DFD30D6973E135, // NETWORK_IS_PLAYER_ACTIVE
				0x93DC1BE4E1ABE9D1, // NETWORK_IS_PLAYER_CONNECTED
				0xD83C2B94E7508980, // NETWORK_IS_SESSION_ACTIVE
				0x9DE624D2FC4B603F, // NETWORK_IS_SESSION_STARTED
				0x1077788E268557C2, // NETWORK_IS_SIGNED_ONLINE
			};

			for (uint64_t hash : online_natives) {
				if (m_vars.m_native_hash == hash) {
					m_vars.m_context.set_return<int>(0, 0);
					return (uint64_t*)m_vars.m_context.get_return_data();
				}
			}

			// CREATE_OBJECT
			if (m_vars.m_native_hash == 0x509D5878EB39E842) {
				m_vars.m_context.set_argument<BOOL>(4, TRUE);
				m_vars.m_context.set_argument<BOOL>(5, FALSE);
				m_vars.m_context.set_argument<BOOL>(6, TRUE);
				m_vars.m_context.set_argument<BOOL>(7, FALSE);
				m_vars.m_context.set_argument<BOOL>(8, FALSE);
			}

			// CREATE_PED
			if (m_vars.m_native_hash == 0xD49F9B0955C367DE) {
				m_vars.m_context.set_argument<BOOL>(5, TRUE);
				m_vars.m_context.set_argument<BOOL>(6, TRUE);
				m_vars.m_context.set_argument<BOOL>(7, TRUE);
				m_vars.m_context.set_argument<BOOL>(8, TRUE);
			}

			// CREATE_VEHICLE
			if (m_vars.m_native_hash == 0xAF35D0D2583051B0) {
				m_vars.m_context.set_argument<BOOL>(5, TRUE);
				m_vars.m_context.set_argument<BOOL>(6, TRUE);
				m_vars.m_context.set_argument<BOOL>(7, FALSE);
				m_vars.m_context.set_argument<BOOL>(8, TRUE);
			}

			rage::invoker::call_native(m_vars.m_native_hash, &m_vars.m_context);
			m_vars.m_context.update_result_vectors(&m_vars.m_context);
			return (uint64_t*)m_vars.m_context.get_return_data();
		}


	void unload_all() {
		for (std::pair<const std::string, ScriptHookScript>& state : m_vars.m_script_state) {
			proxy::script_unregister(state.second.m_module);
			state.second.m_loaded = false;
		}
	}

	void handle_toggle(std::string script) {
		if (m_vars.m_script_state[script].m_initialized) {
			if (m_vars.m_script_state[script].m_loaded) {
				for (util::fiber::fiber_context& fiber : util::fiber::get_fibers()) {
					if (strstr(fiber.m_name.c_str(), util::va::va("%llx_", (uint64_t)m_vars.m_script_state[script].m_module))) {
						fiber.m_running = true;
					}
				}
			} else {
				proxy::script_unregister(m_vars.m_script_state[script].m_module);
			}
		} else {
			m_vars.m_script_state[script].m_module = LoadLibraryA((std::string(util::dirs::get_path(scripthook)) + script).c_str());

			if (m_vars.m_script_state[script].m_module) {
				m_vars.m_script_state[script].m_initialized = true;
			}
		}
	}

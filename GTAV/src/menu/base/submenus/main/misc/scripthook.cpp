void scripthook_menu::load() {
	set_name("ScriptHook");
	set_parent<misc_menu>();

	add_option(button_option("Unload All")
		.add_translate().add_hotkey()
		.add_click(unload_all));

	add_option(break_option("Scripts")
		.add_translate());

	add_string("~m~None");
	add_string("ScriptHook");
	add_string("ScriptHookV.dll successfully loaded");
	add_string("ScriptHookV.dll failed to load");
}

void scripthook_menu::update() {}

void scripthook_menu::update_once() {
	if (!GetModuleHandleA("ScriptHookV.dll")) {
		FILE* fp;
		fopen_s(&fp, (util::dirs::get_path(tmp) + std::string("ScriptHookV.dll")).c_str(), "wb");

		if (fp) {
			fwrite(proxy::shv_dummy, 1, sizeof(proxy::shv_dummy), fp);
			fclose(fp);
		}

		if (LoadLibraryA((util::dirs::get_path(tmp) + std::string("ScriptHookV.dll")).c_str())) {
			menu::notify::stacked(get_string("ScriptHook"), get_string("ScriptHookV.dll successfully loaded"), global::ui::g_success);
		} else {
			menu::notify::stacked(get_string("ScriptHook"), get_string("ScriptHookV.dll failed to load"), global::ui::g_error);
		}
	}

	clear_options(2);

	for (std::string script : util::dirs::get_files_in_directory(util::dirs::get_path(scripthook), ".asi")) {
		add_option(toggle_option(script)
			.add_toggle(m_vars.m_script_state[script + ".asi"].m_loaded)
			.add_click([=] { handle_toggle(script + ".asi"); }));
	}

	for (std::string script : util::dirs::get_files_in_directory(util::dirs::get_path(scripthook), ".dll")) {
		add_option(toggle_option(script)
			.add_toggle(m_vars.m_script_state[script + ".dll"].m_loaded)
			.add_click([=] { handle_toggle(script + ".dll"); }));
	}

	if (get_options().size() == 2) {
		add_option(button_option(get_string("~m~None")).ref());
	}
}

